pub fn loadStyleDark() !void {
    // Load style properties provided
    @setRuntimeSafety(false);
    inline for (dark_style_props) |prop| {
        rgui.rawSetStyle(@enumFromInt(prop.controlId), prop.propertyId, prop.propertyValue);
    }
    @setRuntimeSafety(true);

    // Custom font loading
    const data = try rl.decompressData(&dark_font_data, dark_font_data.len);
    const font_image = rl.Image{
        .data = data.ptr,
        .width = 512,
        .height = 256,
        .mipmaps = 1,
        .format = .uncompressed_gray_alpha,
    };

    // Load texture from image
    var font = std.mem.zeroes(rl.Font);
    font.baseSize = 16;
    font.glyphCount = dark_font_recs.len;
    font.texture = try rl.loadTextureFromImage(font_image);
    rl.unloadImage(font_image);

    // Copy char recs data from global fontRecs
    std.debug.assert(dark_font_recs.len == 189); // expected by the source code
    font.recs = @alignCast(@ptrCast(rl.memAlloc(@intCast(font.glyphCount * @sizeOf(rl.Rectangle)))));
    @memcpy(font.recs, &dark_font_recs);

    font.glyphs = @alignCast(@ptrCast(rl.memAlloc(@intCast(font.glyphCount * @sizeOf(rl.GlyphInfo)))));
    @memcpy(font.glyphs, &dark_font_glyphs);

    rgui.setFont(font);

    // Setup a white rectangle on the font to be used on shapes drawing,
    // it makes possible to draw shapes and text (full UI) in a single draw call
    const font_white_rect = rl.Rectangle{
        .x = 510,
        .y = 254,
        .width = 1,
        .height = 1,
    };
    rl.setShapesTexture(font.texture, font_white_rect);
}
